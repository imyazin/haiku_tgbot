# Функция для получения датасета
def get_haiku_dataset():
    """Возвращает полный датасет хокку"""
    return HAIKU_DATASET

# Функция для получения случайной выборки
def get_random_haikus(count=50):
    """Возвращает случайную выборку хокку"""
    import random
    return random.sample(HAIKU_DATASET, min(count, len(HAIKU_DATASET)))

# Статистика датасета
def dataset_stats():
    """Выводит статистику по датасету"""
    total_haikus = len(HAIKU_DATASET)
    total_words = sum(len(line.split()) for haiku in HAIKU_DATASET for line in haiku)
    avg_words_per_haiku = total_words / total_haikus if total_haikus > 0 else 0
    
    print(f"Общее количество хокку: {total_haikus}")
    print(f"Общее количество слов: {total_words}")
    print(f"Среднее количество слов в хокку: {avg_words_per_haiku:.2f}")
    
    # Подсчет уникальных слов
    all_words = set()
    for haiku in HAIKU_DATASET:
        for line in haiku:
            all_words.update(line.lower().split())
    
    print(f"Уникальных слов в датасете: {len(all_words)}")

